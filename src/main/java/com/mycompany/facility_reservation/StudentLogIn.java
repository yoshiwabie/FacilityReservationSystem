    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        try{
            if (NameTxtFld.getText().isEmpty() || StudNumberTxtFld.getText().isEmpty() || DepTxtFld.getText().isEmpty()) {
            throw new RuntimeException();
        }
            
            User.setName(NameTxtFld);
            User.setStudentNum(StudNumberTxtFld);
            User.setCourse(DepTxtFld);
            
            dispose();
            MenuFrame.setVisible(true);
            //GreetDisplayer.setText("Welcome, " + User.getName());
            
        }catch(RuntimeException e){
             JOptionPane.showMessageDialog(null, "Invalid Login Credentials", "Login Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_LoginButtonActionPerformed

